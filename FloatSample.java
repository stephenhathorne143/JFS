// FloatSample.java
// This program is very similar to IntSample, the
// difference is this program will use a floating point variable.
public class FloatSample {

	public static void main(String[] args) {
    // all variables can be declared and assigned at the same time.
	// The lower case f is syntactically required for floats in Java
		float decimal = 4.24f;
		System.out.println("The value of the floating point number is: " + decimal);
		decimal = 3.14f;
		System.out.println("The value of the floating point number is: " + decimal);
	}

}
