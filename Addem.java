
// Addem.java
// this program is designed to show the student a simple way to add two numbers together
// and display the results.
public class Addem
{

	public static void main(String[] args)
	{
		// set two properties integers -- ints
		int numeralOne = 41;
		int numeralTwo = 1;
		// Add them together placing the outcome into the property (an int) called result
		int result = numeralOne + numeralTwo;
		// Display the property results value to stdout.
		System.out.println("The answer after millions of years of computation, to Life, the Universe and Everything = " + result);

	}

}
